import math

n = int(input('Enter a Natural number:'))
composite_numbers = [
    j for i in range(2,math.ceil(math.sqrt(n)))
    for j in range(i*2, n, i)
]
prime_numbers = [
    x for x in range(2, n)  if x not in composite_numbers
]

# ------- print result -------
print('Composite Numbers (<',n,') :' , composite_numbers)
print('\nPrime Numbers (< ',n,') : ', prime_numbers)